#include "../include/ft.h"

void	ft_print_hex(int n)
{
	if (n >= 16)
		ft_print_hex(n / 16);
	n = n % 16;
	if (n < 10)
		n += '0';
	else
		n += 'a' - 10;
	write(1, &n, 1);
}
